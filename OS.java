//OS class to create OS type object in application class
public class OS {
	
	private String oSystem;
	
	//Declaring constructor
	public OS(String oSystem) {
		
		this.oSystem = oSystem;
	}
	
	//Getter
	public String getOS() {
		
		return this.oSystem ;
	}
	
	//Setter
	public void setOS(String oSystem) {
		
		this.oSystem = oSystem;
	}

}
